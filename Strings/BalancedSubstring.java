package Strings;

//3713. Longest Balanced Substring I
public class BalancedSubstring {
    
    private static boolean checkBalanced(int[] freq) {
        int common = 0;

        for (int f : freq) {
            if (f == 0) continue;

            if (common == 0) {
                common = f;
            } else if (f != common) {
                return false;
            }
        }
        return true;
    }

    public static int longestBalanced(String s) {
        int n = s.length();
        int maxL = 0;

        for (int i = 0; i < n; i++) {
            int[] freq = new int[26]; 

            for (int j = i; j < n; j++) {
                freq[s.charAt(j) - 'a']++;

                if (checkBalanced(freq)) {
                    maxL = Math.max(maxL, j - i + 1);
                }
            }
        }

        return maxL;
    }

    public static void main(String[] args) {
        
        String s = "abbac";

        System.out.println(longestBalanced(s));
    }
}
